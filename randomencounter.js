	var say = function(msg) {
	sendChat("Table", "/w gm &{template:default} {{name=Random Encounter}} {{Desc:="+msg+"}}" );
}

// Pick a random event from an array of events, respecting the weights  of the events.
var resolveMessage = function(table) {
    var total = 0;
    for (i in table) {
        total += table[i].frequency;
    };
    var roll = randomInteger(total);
    log("message total is " + total + " roll " + roll);
    for (i in table) {
        roll -= table[i].frequency;
        if (roll <= 0) {
            return [table[i].message, table[i].lair, table[i].tracks];
        }
    }
    return "something bad happened";
};

